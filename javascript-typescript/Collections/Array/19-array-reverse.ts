//What is the reverse method and how is it used?

/*JavaScript provides a built-in array method called reverse() that reverses the elements of the array in place. 
This method mutates the original array and returns the reversed array.*/

let numbers = [23, 3, 21, 324, 443, 335, 33]

numbers.reverse();

console.log(numbers)
//Output: [ 33, 335, 443, 324, 21, 3, 23 ]