//What is the difference between Set and Array?
//Sets are collections of unique values, while Arrays can contain duplicate values.
//Sets automatically remove duplicates, while Arrays do not.
//Sets are ordered collections, but they do not maintain the order of insertion like Arrays do.
//Indexing is not available in Sets, while Arrays are indexed collections.
//Methods for adding and removing elements differ between Sets and Arrays.
//Sets have methods like .add(), .has(), .delete(), while Arrays use .push() and .indexOf().